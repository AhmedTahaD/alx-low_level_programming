#include <stdlib.h>
#include <time.h>
/*
 * This program will assign a random number
 * to the variable neach time it is executed
 * print whether the number stored in the variable n
 * is positive or negative
*/

/* betty style doc for function main goes there */
int main(void)
{
	int n;

	srand(time(0));
	n = rand() - RAND_MAX / 2;
	/* your code goes there */
	if (n > 0)
	{
		printf("%i is positive\n", n);
	}
	else if (n == 0)
	{
		printf("%i is zero\n", n);
	}
	else
	{
		printf("%i is negative\n", n);
	}
	return (0);
}
